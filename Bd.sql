-- ACTIVIDADES (CORRECTA) 
CREATE TABLE actividades ( 
    numero_actividad BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    nombre VARCHAR(200) NOT NULL, 
    maestro VARCHAR(200) NOT NULL, 
    fecha_entrega DATETIME NOT NULL, 
    estado VARCHAR(50) DEFAULT 'Activo', 
    INDEX idx_numero_actividad (numero_actividad) 
); 
 
-- ENTREGAS (TIPOS IGUALES) 
CREATE TABLE entregas_actividades ( 
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    numero_actividad BIGINT UNSIGNED NOT NULL, 
    numero_control_alumno VARCHAR(20) NOT NULL, 
    nombre_archivo_original VARCHAR(255) NOT NULL, 
    ruta_archivo_servidor VARCHAR(500) NOT NULL, 
    fecha_entrega DATETIME DEFAULT CURRENT_TIMESTAMP, 
    FOREIGN KEY (numero_actividad) REFERENCES actividades(numero_actividad) ON DELETE CASCADE, 
    INDEX idx_numero_actividad (numero_actividad), 
    INDEX idx_numero_control (numero_control_alumno) 
); 
